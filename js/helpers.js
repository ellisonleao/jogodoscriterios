Number.prototype.clamp = function(min, max) {
  return Math.min(Math.max(this, min), max);
};
Object.size = function(obj) {
    var size = 0, key;
    for (key in obj) {
        if (obj.hasOwnProperty(key)) size++;
    }
    return size;
};
